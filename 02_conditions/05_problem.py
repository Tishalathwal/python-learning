# Problem-5: suggest an activity based on the weather(eg Sunny- Go for a walk, Rainy- Read a book, Snowy-Build a snowman)

weather="rainy"

if weather=="sunny":
    print(" Go for a walk")
elif weather=="rainy":
    print("Read a book")
elif weather=="snow":
    print("Build a snowman")
else:
    print("Sleep")